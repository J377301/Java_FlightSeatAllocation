package SeatAllocation;

import java.io.*;

public class Passenger {
    
    //attributes
    String firstName;  
    String lastName;    
    int age;
    String classType;
    String preferredSeat;
    
    //constructors
    public Passenger(){ }
    
    public Passenger(String firstName, String lastName, int age, String classType, String preferredSeat)
    {
        firstName =this.firstName;
        lastName =this.lastName;
        age = this.age;
        classType = this.classType;
        preferredSeat = this.preferredSeat;
    }
    
    //Method
    
}
